#!/usr/bin/env python3

"""
Defines addition of two floats
"""

from typing import TypeVar


def add(a: float, b: float) -> float:
    """
    Adds two float numbers and return their sum.

    Args:
        a(float): first input
        b(float): second input

    Returns:
        float: sum of a and  b
    """
    return a + b
