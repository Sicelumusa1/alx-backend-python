#!/usr/bin/env python3

"""
Defines concatenation of two strings
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenates two input strings and returns the new string

    Args:
        str1 (str): first input string
        str2 (str): second input string

    Return:
        str: Concatenated string
    """
    return str1 + str2
